struct VocabularySeedPayload: Codable {
    let version: Int
    let words: [VocabularySeedRecord]
}

struct VocabularySeedRecord: Codable {
    let id: UUID?
    let germanWord: String
    let article: String?
    let englishTranslation: String
    let level: String
    let category: String
    let version: Int?
